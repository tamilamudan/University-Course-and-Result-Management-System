    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rowCount = model.getRowCount();
        
        for (int i = 0; i < rowCount; i++) {
            String regno = model.getValueAt(i, 0).toString();
            String ccode = model.getValueAt(i, 1).toString();
            String cname = model.getValueAt(i, 2).toString();
            String marks = model.getValueAt(i, 3).toString();

            // Insert into studentmarks table
            String query = "INSERT INTO studentmarks (RegNo, CourseCode, CourseName, Marks) VALUES ('"
                    + regno + "', '" + ccode + "', '" + cname + "', '" + marks + "')";
            st.executeUpdate(query);
        }

        JOptionPane.showMessageDialog(null, "Marks submitted successfully!");
        model.setRowCount(0); // Clear table after submission
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }

    }//GEN-LAST:event_jButton2ActionPerformed

